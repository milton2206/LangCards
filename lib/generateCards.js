import Anthropic from "@anthropic-ai/sdk";

// Человекочитаемые названия для промпта (id из онбординга -> описание).
// name — полное название с англ. дублем для параметров запроса.
// gen — форма родительного падежа для естественных формулировок («носители немецкого»).
const LANGS = {
  de: { name: "немецкий (German)", gen: "немецкого" },
  en: { name: "английский (English)", gen: "английского" },
  el: { name: "греческий (Greek)", gen: "греческого" },
  es: { name: "испанский (Spanish)", gen: "испанского" },
  ru: { name: "русский (Russian)", gen: "русского" },
  uk: { name: "украинский (Ukrainian)", gen: "украинского" },
};
const TOPICS = {
  work: "работа и профессиональная сфера",
  housing: "аренда и поиск жилья",
  doctor: "визит к врачу и здоровье",
  travel: "путешествия",
  daily: "повседневное общение",
  restaurant: "ресторан и кафе",
};
const LEVELS = {
  a1: "уровень A1 (CEFR): самые базовые высокочастотные слова и совсем короткие фразы — как в первых бытовых разговорах",
  a2: "уровень A2 (CEFR): простая повседневная лексика и короткие естественные фразы из обихода",
  b1: "уровень B1 (CEFR): средний уровень; более разнообразная лексика, разговорные обороты, обычные бытовые ситуации",
  b2: "уровень B2 (CEFR): выше среднего; менее частотная лексика, живые разговорные конструкции, оттенки и эмоции",
  c1: "уровень C1 (CEFR): продвинутый; идиомы, устойчивые выражения и тонкие обороты, как у носителей языка",
};

/**
 * Собирает промпт для генерации карточек.
 * Экспортируется отдельно, чтобы можно было проверить формат запроса без вызова API.
 */
export function buildPrompt({
  learnLang,
  nativeLang,
  topic,
  level,
  exclude = [],
  count = 10,
}) {
  const learn = LANGS[learnLang]?.name || learnLang;
  const learnGen = LANGS[learnLang]?.gen || learn;
  const native = LANGS[nativeLang]?.name || nativeLang;
  const nativeGen = LANGS[nativeLang]?.gen || native;
  const topicName = TOPICS[topic] || topic;
  const levelName = LEVELS[level] || level;
  const excludeList = exclude.length
    ? exclude.map((w) => `- ${w}`).join("\n")
    : "(пока нет)";

  return `Сгенерируй ${count} карточек для изучения иностранных слов В КОНТЕКСТЕ.

Параметры:
- Изучаемый язык (на нём слово и пример): ${learn}
- Родной язык пользователя (на него делаем перевод): ${native}
- Тема: ${topicName}
- Уровень: ${levelName}

ГЛАВНОЕ — язык должен быть живым, а не учебниковым:
- Примеры пиши так, как реально говорят носители ${learnGen} в повседневной жизни: настоящие бытовые реплики, естественная лексика, живые ситуации по теме. Никаких сухих искусственно-правильных «учебных» конструкций.
- Отдавай предпочтение фразам, которые человек действительно произнёс бы вслух: короткие реплики, разговорные обороты, обращение к собеседнику. Где это уместно для уровня — разговорные словечки и естественные сокращения.
- Перевод на родной язык (${native}) должен звучать так, как носитель ${nativeGen} сказал бы это сам: передавай смысл и интонацию, а не переводи слово в слово. Никаких калек и дословщины.

ОТБОР СЛОВ:
- Только реальные, общеупотребительные в СОВРЕМЕННОМ языке слова и выражения. Ничего не выдумывай, не бери устаревшее или крайне редкое. Не уверен в слове — выбери более частотное.
- Бери слова, которые действительно часто нужны по теме «${topicName}»: те, что человек встретит и использует в реальной ситуации, а не экзотику.

ТРЕБОВАНИЯ К ПРИМЕРАМ:
- Пример ОБЯЗАТЕЛЬНО содержит само изучаемое слово (можно в другой грамматической форме). Без этого карточка бесполезна.
- Внутри этой пачки примеры делай разными по конструкции и ситуации — не десять однотипных предложений с одинаковым началом.
- Длина по уровню: A1–A2 — короткие простые предложения; B1–B2 — средние, с разговорными оборотами; C1 — сложнее, с тонкими выражениями. Текущий уровень: ${levelName}.

Каждая карточка — объект строго с такими полями:
- "word": слово или выражение на изучаемом языке (${learn}) по теме и уровню. Для существительных указывай артикль, если он есть в языке.
- "translit": подсказка по произношению в квадратных скобках, средствами родного языка (${native}) или в виде IPA.
- "translation": естественный перевод слова на родной язык пользователя (${native}) — тот вариант, который носитель ${nativeGen} действительно использует в этом значении.
- "example": живое предложение на изучаемом языке (${learn}) из реальной повседневной речи, где изучаемое слово стоит прямо в предложении и звучит естественно в контексте темы.
- "exampleTranslation": естественный перевод примера на родной язык (${native}) — так, как это сказали бы носители ${nativeGen}, а не дословно.

НЕ используй эти слова — они уже известны пользователю:
${excludeList}

Верни ТОЛЬКО валидный JSON-массив из ${count} таких объектов. Без markdown, без пояснений, без текста до или после массива.`;
}

// Достаёт JSON-массив из ответа модели (устойчиво к обрамляющему тексту).
function extractJsonArray(text) {
  const start = text.indexOf("[");
  const end = text.lastIndexOf("]");
  if (start === -1 || end === -1 || end < start) {
    throw new Error("В ответе модели не найден JSON-массив.");
  }
  return JSON.parse(text.slice(start, end + 1));
}

/**
 * Генерирует порцию карточек через Claude API.
 * Возвращает массив карточек или бросает Error с понятным message (и .status).
 */
export async function generateCards(params) {
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    const err = new Error(
      "Сервер не настроен: не задан ключ ANTHROPIC_API_KEY.",
    );
    err.status = 500;
    throw err;
  }

  const client = new Anthropic({ apiKey });
  // Haiku — самая дешёвая модель; её качества для генерации карточек достаточно.
  const model = process.env.ANTHROPIC_MODEL || "claude-haiku-4-5";

  const message = await client.messages.create({
    model,
    max_tokens: 4096,
    system:
      "Ты — редактор-носитель, который делает карточки для изучения иностранных слов в контексте. " +
      "Твои примеры звучат как живая повседневная речь носителей, а переводы — как естественный родной язык, а не дословная калька. " +
      "Отвечай только валидным JSON-массивом карточек: никаких размышлений, пояснений или markdown до или после массива.",
    messages: [{ role: "user", content: buildPrompt(params) }],
  });

  const text = message.content
    .filter((b) => b.type === "text")
    .map((b) => b.text)
    .join("");

  const raw = extractJsonArray(text);
  const excludeSet = new Set(
    (params.exclude || []).map((w) => String(w).toLowerCase()),
  );

  const cards = raw
    .filter((c) => c && c.word && c.translation && c.example)
    .filter((c) => !excludeSet.has(String(c.word).toLowerCase()))
    .map((c) => ({
      word: String(c.word),
      translit: c.translit ? String(c.translit) : "",
      translation: String(c.translation),
      example: String(c.example),
      exampleTranslation: c.exampleTranslation
        ? String(c.exampleTranslation)
        : "",
    }));

  if (cards.length === 0) {
    throw new Error("Модель не вернула ни одной карточки. Попробуйте ещё раз.");
  }
  return cards;
}
