import Anthropic from "@anthropic-ai/sdk";

// Человекочитаемые названия для промпта (id из онбординга -> описание).
// name — полное название с англ. дублем для параметров запроса.
// gen — форма родительного падежа для естественных формулировок («носители немецкого»).
const LANGS = {
  de: { name: "немецкий (German)", gen: "немецкого" },
  en: { name: "английский (English)", gen: "английского" },
  el: { name: "греческий (Greek)", gen: "греческого" },
  es: { name: "испанский (Spanish)", gen: "испанского" },
  ru: { name: "русский (Russian)", gen: "русского" },
  uk: { name: "украинский (Ukrainian)", gen: "украинского" },
};
const TOPICS = {
  work: "работа и профессиональная сфера",
  housing: "аренда и поиск жилья",
  doctor: "визит к врачу и здоровье",
  travel: "путешествия",
  daily: "повседневное общение",
  restaurant: "ресторан и кафе",
};
const LEVELS = {
  a1: "уровень A1 (CEFR): самые базовые высокочастотные слова и совсем короткие фразы — как в первых бытовых разговорах",
  a2: "уровень A2 (CEFR): простая повседневная лексика и короткие естественные фразы из обихода",
  b1: "уровень B1 (CEFR): средний уровень; более разнообразная лексика, разговорные обороты, обычные бытовые ситуации",
  b2: "уровень B2 (CEFR): выше среднего; менее частотная лексика, живые разговорные конструкции, оттенки и эмоции",
  c1: "уровень C1 (CEFR): продвинутый; идиомы, устойчивые выражения и тонкие обороты, как у носителей языка",
};

// Человекочитаемый список всех доступных тем — для режима «Удиви меня»,
// где тема не фиксирована и модель мешает разные темы в одной пачке.
const TOPIC_CHOICES = Object.values(TOPICS).join("; ");

// Режим разнообразия («Удиви меня»): тема и уровень НЕ фиксированы, пачка должна
// охватывать разные темы и уровни — чтобы пользователь открыл новую лексику за
// пределами привычной темы. Языковая пара при этом НЕ меняется. Общий блок для
// обоих режимов промпта (слова / контекст носителей).
const RANDOM_RULE = `РЕЖИМ РАЗНООБРАЗИЯ («Удиви меня») — тема и уровень НЕ зафиксированы:
- Распредели карточки по РАЗНЫМ темам из списка: ${TOPIC_CHOICES}. В одной пачке должно быть несколько разных тем — не зацикливайся на одной.
- Мешай РАЗНЫЕ уровни от A1 до C1: и простые частотные слова/фразы, и более сложные.
- Цель — вывести пользователя за пределы привычной темы и дать новую, неожиданную лексику. Языковую пару (что учим / родной) НЕ меняй.`;

// Диакритика — реальная проблема с дешёвой моделью: она иногда роняет ударение/
// умляут в заголовочном слове, хотя в примере пишет верно (напр. «πινω» вместо
// «πίνω»). Требуем полную диакритику явно и одинаковую с примером. Общий блок для
// обоих режимов промпта, чтобы правило не разъезжалось.
const DIACRITICS_RULE = `ОРФОГРАФИЯ И ДИАКРИТИКА (критично):
- Пиши каждое слово и выражение с ПОЛНОЙ и правильной диакритикой языка: греческий — ударение-тонос (ά έ ή ί ό ύ ώ) и диалитика (ϊ ϋ ΐ ΰ); немецкий — умляуты и эсцет (ä ö ü ß); испанский — á é í ó ú ñ ü; французский — é è ê ë à â î ï ô û ù ç œ. Слово без положенной диакритики написано с ОШИБКОЙ — так нельзя.
- В поле «word» слово должно быть написано ТОЧНО с той же диакритикой, как оно стоит в поле «example». Никогда не убирай ударение и надстрочные знаки в заголовочной форме.`;

// Транскрипция нужна тем, кто не читает письменность изучаемого языка. Если у него
// своя система письма (кириллица/греческий алфавит), а родной пользуется латиницей,
// даём читаемую ЛАТИНСКУЮ транслитерацию с ударением (напр. «спасибо» → [spa-SEE-ba]).
// Одна формулировка на оба режима — чтобы правило не разъезжалось. native — название
// родного языка (для указания, чьими средствами давать подсказку).
function translitRule(native) {
  return `подсказка по произношению в квадратных скобках — читаемая транскрипция для того, кто НЕ знает письменность изучаемого языка. Если у изучаемого языка своя письменность (кириллица, греческий алфавит), а родной (${native}) пользуется латиницей — давай транслитерацию ЛАТИНИЦЕЙ с обозначенным ударением: ударный слог заглавными, слоги через дефис (например «спасибо» → [spa-SEE-ba]). Если письменности совпадают или родной сам нелатинский — привычная фонетическая подсказка средствами родного языка (${native}) или IPA.`;
}

// Разрешает «сырые» параметры онбординга в человекочитаемые куски для промпта.
// Общая шапка для обоих режимов (слова / контекст носителей).
function resolveParams({
  learnLang,
  nativeLang,
  topic,
  level,
  exclude = [],
  random = false,
}) {
  return {
    learn: LANGS[learnLang]?.name || learnLang,
    learnGen: LANGS[learnLang]?.gen || LANGS[learnLang]?.name || learnLang,
    native: LANGS[nativeLang]?.name || nativeLang,
    nativeGen: LANGS[nativeLang]?.gen || LANGS[nativeLang]?.name || nativeLang,
    // В режиме «Удиви меня» тема/уровень не фиксированы — заголовок отражает это,
    // а конкретику даёт RANDOM_RULE. Иначе — обычные названия темы и уровня.
    topicName: random ? "случайный набор разных тем" : TOPICS[topic] || topic,
    levelName: random
      ? "случайный набор уровней A1–C1"
      : LEVELS[level] || level,
    excludeList: exclude.length
      ? exclude.map((w) => `- ${w}`).join("\n")
      : "(пока нет)",
    random,
  };
}

/**
 * Собирает промпт для генерации карточек.
 * manual: true — одна карточка по введённому пользователем слову (params.word).
 * mode: "words" (обычные слова, по умолчанию) | "idioms" («Контекст носителей»).
 * Экспортируется отдельно, чтобы можно было проверить формат запроса без вызова API.
 */
export function buildPrompt(params) {
  if (params.manual) return buildManualPrompt(params);
  return params.mode === "idioms"
    ? buildIdiomsPrompt(params)
    : buildWordsPrompt(params);
}

// Режим «слова»: отдельные слова/выражения по теме и уровню, каждое в контексте.
