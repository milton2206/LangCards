import Anthropic from "@anthropic-ai/sdk";

// Человекочитаемые названия для промпта (id из онбординга -> описание).
// name — полное название с англ. дублем для параметров запроса.
// gen — форма родительного падежа для естественных формулировок («носители немецкого»).
const LANGS = {
  de: { name: "немецкий (German)", gen: "немецкого" },
  en: { name: "английский (English)", gen: "английского" },
  el: { name: "греческий (Greek)", gen: "греческого" },
  es: { name: "испанский (Spanish)", gen: "испанского" },
  ru: { name: "русский (Russian)", gen: "русского" },
  uk: { name: "украинский (Ukrainian)", gen: "украинского" },
};
const TOPICS = {
  work: "работа и профессиональная сфера",
  housing: "аренда и поиск жилья",
  doctor: "визит к врачу и здоровье",
  travel: "путешествия",
  daily: "повседневное общение",
  restaurant: "ресторан и кафе",
};
const LEVELS = {
  a1: "уровень A1 (CEFR): самые базовые высокочастотные слова и совсем короткие фразы — как в первых бытовых разговорах",
  a2: "уровень A2 (CEFR): простая повседневная лексика и короткие естественные фразы из обихода",
  b1: "уровень B1 (CEFR): средний уровень; более разнообразная лексика, разговорные обороты, обычные бытовые ситуации",
  b2: "уровень B2 (CEFR): выше среднего; менее частотная лексика, живые разговорные конструкции, оттенки и эмоции",
  c1: "уровень C1 (CEFR): продвинутый; идиомы, устойчивые выражения и тонкие обороты, как у носителей языка",
};

// Диакритика — реальная проблема с дешёвой моделью: она иногда роняет ударение/
// умляут в заголовочном слове, хотя в примере пишет верно (напр. «πινω» вместо
// «πίνω»). Требуем полную диакритику явно и одинаковую с примером. Общий блок для
// обоих режимов промпта, чтобы правило не разъезжалось.
const DIACRITICS_RULE = `ОРФОГРАФИЯ И ДИАКРИТИКА (критично):
- Пиши каждое слово и выражение с ПОЛНОЙ и правильной диакритикой языка: греческий — ударение-тонос (ά έ ή ί ό ύ ώ) и диалитика (ϊ ϋ ΐ ΰ); немецкий — умляуты и эсцет (ä ö ü ß); испанский — á é í ó ú ñ ü; французский — é è ê ë à â î ï ô û ù ç œ. Слово без положенной диакритики написано с ОШИБКОЙ — так нельзя.
- В поле «word» слово должно быть написано ТОЧНО с той же диакритикой, как оно стоит в поле «example». Никогда не убирай ударение и надстрочные знаки в заголовочной форме.`;

// Транскрипция нужна тем, кто не читает письменность изучаемого языка. Если у него
// своя система письма (кириллица/греческий алфавит), а родной пользуется латиницей,
// даём читаемую ЛАТИНСКУЮ транслитерацию с ударением (напр. «спасибо» → [spa-SEE-ba]).
// Одна формулировка на оба режима — чтобы правило не разъезжалось. native — название
// родного языка (для указания, чьими средствами давать подсказку).
function translitRule(native) {
  return `подсказка по произношению в квадратных скобках — читаемая транскрипция для того, кто НЕ знает письменность изучаемого языка. Если у изучаемого языка своя письменность (кириллица, греческий алфавит), а родной (${native}) пользуется латиницей — давай транслитерацию ЛАТИНИЦЕЙ с обозначенным ударением: ударный слог заглавными, слоги через дефис (например «спасибо» → [spa-SEE-ba]). Если письменности совпадают или родной сам нелатинский — привычная фонетическая подсказка средствами родного языка (${native}) или IPA.`;
}

// Разрешает «сырые» параметры онбординга в человекочитаемые куски для промпта.
// Общая шапка для обоих режимов (слова / контекст носителей).
function resolveParams({ learnLang, nativeLang, topic, level, exclude = [] }) {
  return {
    learn: LANGS[learnLang]?.name || learnLang,
    learnGen: LANGS[learnLang]?.gen || LANGS[learnLang]?.name || learnLang,
    native: LANGS[nativeLang]?.name || nativeLang,
    nativeGen: LANGS[nativeLang]?.gen || LANGS[nativeLang]?.name || nativeLang,
    topicName: TOPICS[topic] || topic,
    levelName: LEVELS[level] || level,
    excludeList: exclude.length
      ? exclude.map((w) => `- ${w}`).join("\n")
      : "(пока нет)",
  };
}

/**
 * Собирает промпт для генерации карточек.
 * mode: "words" (обычные слова, по умолчанию) | "idioms" («Контекст носителей»).
 * Экспортируется отдельно, чтобы можно было проверить формат запроса без вызова API.
 */
export function buildPrompt(params) {
  return params.mode === "idioms"
    ? buildIdiomsPrompt(params)
    : buildWordsPrompt(params);
}

// Режим «слова»: отдельные слова/выражения по теме и уровню, каждое в контексте.
function buildWordsPrompt(params) {
  const { learn, learnGen, native, nativeGen, topicName, levelName, excludeList } =
    resolveParams(params);
  const count = params.count ?? 10;

  return `Сгенерируй ${count} карточек для изучения иностранных слов В КОНТЕКСТЕ.

Параметры:
- Изучаемый язык (на нём слово и пример): ${learn}
- Родной язык пользователя (на него делаем перевод): ${native}
- Тема: ${topicName}
- Уровень: ${levelName}

ГЛАВНОЕ — язык должен быть живым, а не учебниковым:
- Примеры пиши так, как реально говорят носители ${learnGen} в повседневной жизни: настоящие бытовые реплики, естественная лексика, живые ситуации по теме. Никаких сухих искусственно-правильных «учебных» конструкций.
- Отдавай предпочтение фразам, которые человек действительно произнёс бы вслух: короткие реплики, разговорные обороты, обращение к собеседнику. Где это уместно для уровня — разговорные словечки и естественные сокращения.
- Перевод на родной язык (${native}) должен звучать так, как носитель ${nativeGen} сказал бы это сам: передавай смысл и интонацию, а не переводи слово в слово. Никаких калек и дословщины.

ОТБОР СЛОВ:
- Только реальные, общеупотребительные в СОВРЕМЕННОМ языке слова и выражения. Ничего не выдумывай, не бери устаревшее или крайне редкое. Не уверен в слове — выбери более частотное.
- Бери слова, которые действительно часто нужны по теме «${topicName}»: те, что человек встретит и использует в реальной ситуации, а не экзотику.

${DIACRITICS_RULE}

ТРЕБОВАНИЯ К ПРИМЕРАМ:
- Пример ОБЯЗАТЕЛЬНО содержит само изучаемое слово (можно в другой грамматической форме). Без этого карточка бесполезна.
- Внутри этой пачки примеры делай разными по конструкции и ситуации — не десять однотипных предложений с одинаковым началом.
- Длина по уровню: A1–A2 — короткие простые предложения; B1–B2 — средние, с разговорными оборотами; C1 — сложнее, с тонкими выражениями. Текущий уровень: ${levelName}.

Каждая карточка — объект строго с такими полями:
- "word": слово или выражение на изучаемом языке (${learn}) по теме и уровню, с ПОЛНОЙ правильной диакритикой (ударения, умляуты и т.п.) — ровно так же, как оно написано в «example». Для существительных указывай артикль, если он есть в языке.
- "translit": ${translitRule(native)}
- "translation": естественный перевод слова на родной язык пользователя (${native}) — тот вариант, который носитель ${nativeGen} действительно использует в этом значении.
- "example": живое предложение на изучаемом языке (${learn}) из реальной повседневной речи, где изучаемое слово стоит прямо в предложении и звучит естественно в контексте темы.
- "exampleTranslation": естественный перевод примера на родной язык (${native}) — так, как это сказали бы носители ${nativeGen}, а не дословно.

НЕ используй эти слова — они уже известны пользователю:
${excludeList}

Верни ТОЛЬКО валидный JSON-массив из ${count} таких объектов. Без markdown, без пояснений, без текста до или после массива.`;
}

// Режим «Контекст носителей»: идиомы, устойчивые обороты и разговорные фразы —
// то, что носители реально говорят и что не даёт учебник. Отдельное поле "note"
// поясняет уместность/регистр. Механика карточек та же (word — лицо карточки).
function buildIdiomsPrompt(params) {
  const { learn, learnGen, native, nativeGen, topicName, levelName, excludeList } =
    resolveParams(params);
  const count = params.count ?? 10;

  return `Сгенерируй ${count} карточек «Контекст носителей»: это НЕ отдельные слова, а живые выражения, которые носители ${learnGen} реально используют в повседневной жизни.

Параметры:
- Изучаемый язык (на нём выражение и пример): ${learn}
- Родной язык пользователя (на него объясняем смысл): ${native}
- Тема/ситуация: ${topicName}
- Уровень: ${levelName}

ЧТО ГЕНЕРИРОВАТЬ — только живое, чего не даёт учебник:
- идиомы и устойчивые обороты;
- разговорные фразы для конкретных ситуаций по теме;
- то, что НЕ переводится дословно (смысл не складывается из отдельных слов).
Не бери сухие словарные слова, термины и школьные штампы — только то, что человек реально услышит и скажет в жизни.

КАЧЕСТВО:
- Только настоящие, употребительные в СОВРЕМЕННОМ языке выражения. Ничего не выдумывай, не бери устаревшее или книжное (кроме случаев, когда сам помечаешь это в «note»). Не уверен — бери более частотное и живое.
- Держи уровень (${levelName}): на A1–A2 — простые частотные фразы, на B1–B2 — разговорные обороты, на C1 — идиомы и тонкие выражения.
- Внутри пачки выражения должны быть РАЗНЫМИ по типу и ситуации — не однотипные обороты подряд.
- Пример ОБЯЗАТЕЛЬНО содержит само выражение в естественном контексте.

${DIACRITICS_RULE}

Каждая карточка — объект строго с такими полями:
- "word": само выражение/идиома на изучаемом языке (${learn}) — это лицевая сторона карточки. Пиши его с ПОЛНОЙ правильной диакритикой (ударения, умляуты и т.п.), ровно так же, как оно написано в «example».
- "translit": ${translitRule(native)} Если подсказка не нужна — пустая строка.
- "translation": СМЫСЛ выражения на родном языке (${native}) — что оно на самом деле значит и как это сказали бы носители ${nativeGen}. НЕ дословный перевод.
- "example": живой пример употребления на изучаемом языке (${learn}), где выражение звучит естественно.
- "exampleTranslation": естественный перевод примера на родной язык (${native}) — по смыслу, а не дословно.
- "note": короткая пометка на родном языке (${native}) — когда и как это уместно употребить, плюс регистр: например «неформально», «так говорят в быту», «грубовато», «звучит иронично», «слегка устарело». Одно короткое предложение. Если добавить нечего — пустая строка.

НЕ используй эти выражения — они уже известны пользователю:
${excludeList}

Верни ТОЛЬКО валидный JSON-массив из ${count} таких объектов. Без markdown, без пояснений, без текста до или после массива.`;
}

// Достаёт JSON-массив из ответа модели (устойчиво к обрамляющему тексту).
function extractJsonArray(text) {
  const start = text.indexOf("[");
  const end = text.lastIndexOf("]");
  if (start === -1 || end === -1 || end < start) {
    throw new Error("В ответе модели не найден JSON-массив.");
  }
  return JSON.parse(text.slice(start, end + 1));
}

/**
 * Генерирует порцию карточек через Claude API.
 * Возвращает массив карточек или бросает Error с понятным message (и .status).
 */
export async function generateCards(params) {
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    const err = new Error(
      "Сервер не настроен: не задан ключ ANTHROPIC_API_KEY.",
    );
    err.status = 500;
    throw err;
  }

  const client = new Anthropic({ apiKey });
  // Haiku — самая дешёвая модель; её качества для генерации карточек достаточно.
  const model = process.env.ANTHROPIC_MODEL || "claude-haiku-4-5";

  // Персона зависит от режима: обычные слова или «Контекст носителей» (идиомы/фразы).
  const diacriticsLine =
    "Пиши каждое слово с полной правильной диакритикой языка (греческий тонос ά έ ή ί ό ύ ώ, немецкие ä ö ü ß, испанские/французские надстрочные знаки) — и в заголовочном слове, и в примере одинаково. ";
  const system =
    params.mode === "idioms"
      ? "Ты — редактор-носитель, который собирает живые выражения языка: идиомы, устойчивые обороты и разговорные фразы, которые носители реально используют в повседневной жизни. " +
        "Ты объясняешь смысл и уместность выражения, а не даёшь дословный перевод. " +
        diacriticsLine +
        "Отвечай только валидным JSON-массивом карточек: никаких размышлений, пояснений или markdown до или после массива."
      : "Ты — редактор-носитель, который делает карточки для изучения иностранных слов в контексте. " +
        "Твои примеры звучат как живая повседневная речь носителей, а переводы — как естественный родной язык, а не дословная калька. " +
        diacriticsLine +
        "Отвечай только валидным JSON-массивом карточек: никаких размышлений, пояснений или markdown до или после массива.";

  const message = await client.messages.create({
    model,
    max_tokens: 4096,
    system,
    messages: [{ role: "user", content: buildPrompt(params) }],
  });

  const text = message.content
    .filter((b) => b.type === "text")
    .map((b) => b.text)
    .join("");

  const raw = extractJsonArray(text);
  const excludeSet = new Set(
    (params.exclude || []).map((w) => String(w).toLowerCase()),
  );

  const cards = raw
    .filter((c) => c && c.word && c.translation && c.example)
    .filter((c) => !excludeSet.has(String(c.word).toLowerCase()))
    .map((c) => ({
      word: String(c.word),
      translit: c.translit ? String(c.translit) : "",
      translation: String(c.translation),
      example: String(c.example),
      exampleTranslation: c.exampleTranslation
        ? String(c.exampleTranslation)
        : "",
      // Пометка об уместности/регистре — только у режима «Контекст носителей».
      // Для обычных слов остаётся пустой строкой.
      note: c.note ? String(c.note) : "",
    }));

  if (cards.length === 0) {
    throw new Error("Модель не вернула ни одной карточки. Попробуйте ещё раз.");
  }
  return cards;
}
